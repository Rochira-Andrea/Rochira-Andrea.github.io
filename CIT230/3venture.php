                        <h4>Trip Description</h4>
                        <p>You will be surrounded by wonderful river guides, where your safety and comfort is our top priority as you river raft through some of the most beautiful places in Idaho on the Salmon “River of No Return”. Experience both calm waters along with thrilling rapids as you journey down the river.</p>
                        <p>Immersed in nature you may see all kinds of wildlife including bears, bighorn sheep, bald and golden eagles, deer, and many more. Your guide will share history of the Native Americans and the early settlement of Idaho as you travel through this amazing Salmon River canyon. You will have the opportunity to learn about the gold mining history of Idaho and tour abandoned gold mines. Enjoy great food and camp riverside on this beautiful and exhilarating stretch of the Main Salmon River.</p>
                        <h4>Itinerary</h4>
                        <p><b><u>Day 1:</u></b> Arrive Idaho Adventures’ headquarters at 7:00 am for trip orientations and pack for your river adventure. Depart Idaho Adventures at 9:00 am to the Spring Creek put–in to begin your two day fun filled river trip. After a short scenic drive, you will arrive around 9:45 am to the put-in where you will get a quick safety briefing at the launch site before you depart for two days of fun filled river adventure.</p>
                        <p>Enjoy a Goldmine Tour around 11:00 am, snacks and beverages provided. Your guide will provide a tour of the operation and equipment demonstrating gold panning and explaining how the ore was processed in the old days. Step inside the mine shaft and be reminded of how difficult life was in the 1800’s.</p>
                        <p>Once again, board your rafts to continue downriver for your day on the “River of No Return” plunging through class II and III rapids such as Pine Creek, Little Falls, Dutch Oven I, Dutch Oven II, Bone Crusher and more. We will stop along the way for another fantastic riverside lunch. After lunch we will get back on the water with more class II and III rapids and breathtaking scenery arriving at your river camp around 5:00 pm. The guides will dazzle you with fireside grilling and Dutch oven river cuisine, while you enjoy fireside conversations and the night skies with friends and family.</p>
                        <p><b><u>Day 2:</u></b> Guides will start waking you up around 7:30 with freshly brewed gourmet coffee and tea. After a delicious breakfast you will enjoy another day on the Salmon River plunging through rapids such as Long Tom, Kramer, and Kitchen Creek. We’ll stop for a riverside lunch along the way as you enter the deepest section of the river canyon, 2nd deepest in North America.</p>
                        <p>Continue rafting after lunch to the take-out at either Cache Bar or Corn Creek depending on your groups preferences. Expect to arrive back to Salmon around 6:00 pm.</p>
